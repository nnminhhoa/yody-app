const getAllProductSale = () => {
  const productSale = [];
  products.forEach((e) => {
    const itemSale = e.product.filter((e) => e.sale !== 0);
    return productSale.push(itemSale);
  });
  return productSale[0].concat(...productSale);
};

const getProductByInfo = (info) => {
  const item = [];
  products.filter((e) => {
    if (e.info === info) {
      return item.push(e);
    }
    return item;
  });
  const product = [];
  item.forEach((e) => {
    return product.push(e.product);
  });
  return product[0];
};

const dataProducts = {
  getAllProductSale,
  getProductByInfo,
};

export default dataProducts;
